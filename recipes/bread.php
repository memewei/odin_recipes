    <h1>Pain au Chocolat</h1>
    <img src="../img/pain-au-chocolat.jpeg" alt="pain-au-chocolat">
    <h2>Description</h2>
    <p>Pain au chocolat is a type of Viennoiserie pastry consisting of a cuboid-shaped piece of
        yeast-leavened laminated dough, similar in texture to a puff pastry, with one or two pieces of dark chocolate in the center.
    </p>
    <p>Usually sold or served warm from the oven, they are commonly sold alongside croissants in French bakeries and supermarkets.</p>
    <h2>Ingredients</h2>
    <ul>
        <li>500g puff pastry block</li>
        <li>100g dark chocolate, broken into small pieces</li>
        <li>1 egg, lightly beaten</li>
        <li>Icing sugar, for dusting</li>
    </ul>
    <h2>Steps</h2>
    <ol>
        <li>Preheat the oven to 200°C/180°C fan/gas mark 6.</li>
        <li>Lightly dust work surface with icing sugar and roll out the pastry into a rectangle.</li>
        <li>Cut into three strips lengthways, and then cut each strip into 4 smaller rectangles.</li>
        <li>Lay a line of chocolate in the middle of each rectangle. Fold pastry over the chocolate and firmly press on the edges to seal.</li>
        <li>Place the pastries on baking trays, making sure they are well spaced apart.</li>
        <li>Brush each pastry with the lightly beaten egg, and then dust with icing sugar.</li>
        <li>Bake in the preheated oven for about 20-25 minutes until golden brown.</li>
        <li>Serve warm and enjoy!</li>
    </ol>
